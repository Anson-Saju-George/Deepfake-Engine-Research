def build_loader_kwargs(batch_size, shuffle=False, drop_last=False):
    """Centralize DataLoader runtime settings for raw-video training."""
    kwargs = {
        "batch_size": batch_size,
        "shuffle": shuffle,
        "num_workers": NUM_WORKERS,
        "pin_memory": True,
        "drop_last": drop_last,
    }
    if NUM_WORKERS > 0:
        kwargs["persistent_workers"] = True
        kwargs["prefetch_factor"] = DEFAULT_PREFETCH_FACTOR
    return kwargs

