"""Unified dataset loading utilities for image and video deepfake experiments.

This module is the active data entrypoint used by training, evaluation, and data
inspection scripts.

Primary training path:
- image datasets feed image-only models
- raw video datasets feed video-only models
  - `mode="single"` acts as the spatial baseline on video data
  - `mode="sequence"` acts as the spatial+temporal path

Optional support remains for extracted frame folders, but frame materialization
is auxiliary and is not required for the default raw-video workflow.
"""

