class DeepFakeDataset(Dataset):
    def __init__(self, samples, transform=None, mode="single", seq_len=8, clip_sampling="random"):
        self.samples = samples
        self.transform = transform
        self.mode = mode
        self.seq_len = seq_len
        self.clip_sampling = clip_sampling

    def __len__(self):
        return len(self.samples)

    def _read_frame(self, cap, frame_id):
        cap.set(cv2.CAP_PROP_POS_FRAMES, frame_id)
        ret, frame = cap.read()
        return Image.fromarray(cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)) if ret else None

    def _read_frame_dir_image(self, frame_files, index):
        if not frame_files:
            return Image.new("RGB", (224, 224))
        clamped = max(0, min(index, len(frame_files) - 1))
        return Image.open(frame_files[clamped]).convert("RGB")

    def _single_index(self, total_frames):
        if total_frames <= 0:
            return 0
        if self.clip_sampling == "center":
            return total_frames // 2
        return random.randint(0, total_frames - 1)

    def _list_frame_files(self, frame_dir):
        frame_dir = Path(frame_dir)
        return sorted(
            str(item)
            for item in frame_dir.iterdir()
            if item.is_file() and item.suffix.lower() in IMAGE_EXTS
        )

    def _contiguous_indices(self, total_frames):
        if total_frames <= 0:
            return [0] * self.seq_len
        if total_frames <= self.seq_len:
            return list(range(total_frames)) + [max(total_frames - 1, 0)] * (self.seq_len - total_frames)
        if self.clip_sampling == "center":
            start = max((total_frames - self.seq_len) // 2, 0)
        else:
            start = random.randint(0, total_frames - self.seq_len)
        return list(range(start, start + self.seq_len))

    def __getitem__(self, idx):
        path, label, dtype = self.samples[idx]
        try:
            if self.mode == "single":
                if dtype == "image":
                    img = Image.open(path).convert("RGB")
                elif dtype == "video":
                    if HAS_DECORD:
                        vr = VideoReader(path, ctx=cpu(0))
                        img = Image.fromarray(vr[self._single_index(len(vr))].asnumpy())
                    else:
                        cap = cv2.VideoCapture(path)
                        img = self._read_frame(cap, self._single_index(int(cap.get(cv2.CAP_PROP_FRAME_COUNT))))
                        cap.release()
                elif dtype == "frame":
                    frame_files = self._list_frame_files(path)
                    img = self._read_frame_dir_image(frame_files, self._single_index(len(frame_files)))
                if self.transform: img = self.transform(img)
                return img, label

            elif self.mode == "sequence":
                if dtype == "frame":
                    frame_files = self._list_frame_files(path)
                    indices = self._contiguous_indices(len(frame_files))
                    frames = []
                    for i in indices:
                        img = self._read_frame_dir_image(frame_files, i)
                        if self.transform:
                            img = self.transform(img)
                        frames.append(img)
                    return torch.stack(frames), label
                if HAS_DECORD:
                    vr = VideoReader(path, ctx=cpu(0))
                    indices = self._contiguous_indices(len(vr))
                    frames = [Image.fromarray(f.asnumpy()) for f in vr.get_batch(indices)]
                    if self.transform: frames = [self.transform(f) for f in frames]
                    return torch.stack(frames), label
                else:
                    cap = cv2.VideoCapture(path)
                    total_frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
                    indices = self._contiguous_indices(total_frames)
                    frames = []
                    for i in indices:
                        img = self._read_frame(cap, i)
                        if img is None: img = frames[-1] if frames else Image.new('RGB', (224, 224))
                        if self.transform: img = self.transform(img)
                        frames.append(img)
                    cap.release()
                    return torch.stack(frames), label
            raise ValueError(f"Unsupported mode: {self.mode}")
        except Exception:
            shape = (3, 224, 224) if self.mode == "single" else (self.seq_len, 3, 224, 224)
            return torch.zeros(shape), label

